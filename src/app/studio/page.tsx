const SPEC_TAGS = ['spec', 'prd', 'strategy-paper']

function normalizeTags(s: RawSeed): string[] {
  const raw = s.tags ?? s.seed_metadata?.tags ?? s.metadata?.tags ?? []
  const arr = Array.isArray(raw) ? raw : String(raw).split(',')
  return arr.map(t => t.trim().toLowerCase()).filter(Boolean)
}

function isSpecSeed(s: RawSeed): boolean {
  const type = (s.seed_type || s.type || '').toLowerCase()
  if (type === 'spec') return true
  const content = s.content || s.text || s.summary || ''
  // Must have meaningful PRD content (not just a short agent output)
  if (content.length < 500) return false
  return normalizeTags(s).some(t => SPEC_TAGS.includes(t))
}

function seedToPRD(s: RawSeed): PRDItem {
  const content = s.content || s.text || s.summary || ''
  const meta = s.seed_metadata || s.metadata || {}
  return {
    id: s.id || s.notion_id || `seed_${Math.random().toString(36).slice(2)}`,
    title: s.title || content.split('\n')[0]?.replace(/^#+\s*/, '').slice(0, 80) || 'Untitled spec',
    content, createdAt: s.created_at || s.created || new Date().toISOString(), source: 'garden',
    diagramUrl: meta.diagram_url, diagramMermaid: meta.diagram_mermaid, buildStatus: meta.build_status, prUrl: meta.build_pr_url,
    autoGenerated: !!meta.auto_generated, visionStatus: meta.vision_status,
    quality: meta.quality, sourcePaperId: meta.source_paper_id,
    designVisionId: meta.design_vision_id, designVisionTitle: meta.design_vision_title,
    productId: meta.product_id, pillarId: meta.pillar_id, attachment: meta.attachment,
    serves: meta.serves, overlaps: meta.overlaps,
  }
}

